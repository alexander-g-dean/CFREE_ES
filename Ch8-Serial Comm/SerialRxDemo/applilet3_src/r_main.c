void  main(void)
{
    /* Start user code. Do not edit comment generated here */
  uint8_t inbuf[8];
	uint8_t n;
	
	
	LCDInit();           
	LCDStringLinePos("Received:",0,0);
	R_UART0_Start();
	
	while (1) {
		// erase buffer
		for (n=0; n<8; n++)
			inbuf[n] = '\0';
		
		// Prepare to receive four characters
		G_UART_ReceivingData = 1;
		R_UART0_Receive(inbuf, 4);

		// wait for rx to finish
	 	while (G_UART_ReceivingData)
		 	;

		// Display the received data
		LCDStringLinePos((char const *) inbuf, 2, 2);
	}	
  /* End user code. Do not edit comment generated here */
}

